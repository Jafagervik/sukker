//! Module containing often used mathematical constants

/// =================================
///  F32
/// =================================

/// INF = infinity
pub const INF: f32 = f32::INFINITY;

/// NAN= Not A Number
pub const NAN: f32 = f32::NAN;

/// E = Euler constant = 2.7182818...
pub const E32: f32 = std::f32::consts::E;

/// MAX = max value an f32 can be
pub const MAX: f32 = f32::MAX;

/// MIN = min value an f32 can be
pub const MIN: f32 = f32::MIN;

/// f32 representation of the mathematical constant pi
pub const PI: f32 = std::f32::consts::PI;

/// =================================
///  F64
/// =================================

/// INF = infinity
pub const INF64: f64 = f64::INFINITY;

/// NAN= Not A Number
pub const NANF64: f64 = f64::NAN;

/// E = Euler constant = 2.7182818...
pub const E64: f64 = std::f64::consts::E;

/// MAX = max value an f64 can be
pub const MAXF64: f64 = f64::MAX;

/// MIN = min value an f64 can be
pub const MINF64: f64 = f64::MIN;

/// f64 representation of the mathematical constant pi
pub const PI64: f64 = std::f64::consts::PI;

/// =================================
///     I32
/// =================================

/// MAX = max value an i32 can be
pub const MAXI32: i32 = i32::MAX;

/// MIN = min value an i32 can be
pub const MINI32: i32 = i32::MIN;

/// =================================
///     I64
/// =================================

/// MAX = max value an i64 can be
pub const MAXI64: i64 = i64::MAX;

/// MIN = min value an i64 can be
pub const MINI64: i64 = i64::MIN;
